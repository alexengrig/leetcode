package dev.alexengrig.leetcode._206_reverse_linked_list;

interface Task {
    ListNode reverseList(ListNode head);

    class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }
}
